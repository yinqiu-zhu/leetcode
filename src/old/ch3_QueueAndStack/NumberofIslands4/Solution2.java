  private int line, row;
  char[][] _grid;

  public int numIslands(char[][] grid) {

    if (grid == null || grid.length == 0) return 0;

    _grid = grid;
     line = grid.length;
     row = grid[0].length;
     int count =0;

     for (int i = 0; i < line; i++){
       for (int j = 0; j < row; j++){
         if (_grid[i][j] == '1'){
           count ++;
           DFS(i,j);
         }
       }
     }

    return count;
  }

  public void DFS(int a, int b){
    if (a < line && b < row && a >= 0 && b >= 0 && _grid[a][b] == '1') {
            _grid[a][b] = '0';
            DFS(a-1,b);
            DFS(a,b-1);
            DFS(a,b+1);
            DFS(a+1,b);
        }
  }

